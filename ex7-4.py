def add(x, y):
    return x + y
def mul(x, y):
    return x * y
def sub(x, y):
    return x - y
def div(x, y):
    return x / y   
x = int(input("please input num x:"))
y = int(input("please input num y:"))
print("x+y =",add(x,y))
print("x*y =",mul(x,y))
print("x-y =",sub(x,y))
print("x/y =",div(x,y))
